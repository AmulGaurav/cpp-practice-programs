#include <iostream>
using namespace std;

int main() {

int a , b ;

cout<<"Enter the value of a:\n";     /* '<<' is called insertion operator */

cin>>a;     /* '>>' is called extraction operator */


cout<<"Enter the value of b:\n";     /* '<<' is called insertion operator */

cin>>b;     /* '>>' is called extraction operator */


cout<<"The sum is "<<a+b;

    return 0;
}